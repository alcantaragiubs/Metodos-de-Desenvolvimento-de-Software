import Head from "next/head";
import styles from "../../styles/Home.module.css";
import Layout from "../../component/layout";
import {apiRequest} from "../../util/apiRequest";
import { useRouter } from "next/router";
import React, { useState, ChangeEvent, useEffect } from "react";
import Table from "@mui/material/Table";
import TableHead from "@mui/material/TableHead";
import TableRow from "@mui/material/TableRow";
import TableCell from "@mui/material/TableCell";
import TableBody from "@mui/material/TableBody";
import Button from "@mui/material/Button";
import IconButton from "@mui/material/IconButton";
import ModeEditIcon from "@mui/icons-material/ModeEdit";
import BuildCircleIcon from "@mui/icons-material/BuildCircle";

export default function PortalDaTurma() {
  const [turma, setTurma] = useState<any>([]);
  const [open, setOpen] = useState(false);
  const router = useRouter();
  
  async function getTurma() {
    const resTurmas = await apiRequest.get("turma");
    if (resTurmas.data) {
      setTurma(resTurmas.data);
    } 
  }
  useEffect(() => {
    getTurma();
  }, []);
  return (
    <div className={styles.container}>
      <Head>
        <title>Galdi</title>
        <meta name="description" content="Generated by meio a meio" />
      </Head>
      <Layout>
        <main className={styles.main}>
          <p className={styles.description}>Turmas</p>
          <div
            style={{
              alignItems: "center",
              display: "flex",
              flexDirection: "column",
            }}
          >
            <Table
              sx={{ minWidth: 650 }}
              size="small"
              aria-label="a dense table"
            >
              <TableHead>
                <TableRow>
                  <TableCell>Id</TableCell>
                  <TableCell align="center">Nome</TableCell>
                  <TableCell align="center">Editar</TableCell>
                </TableRow>
              </TableHead>
              <TableBody>
                {turma.map((row, index) => (
                  <TableRow key={index}>
                    <TableCell component="th" scope="row">
                      {row.id}
                    </TableCell>
                    <TableCell align="center">
                      {row.nome_turma}
                      <IconButton
                        aria-label="expand row"
                        size="small"
                        onClick={() =>
                          router.push({
                            pathname: "/turma/[detalhes]",
                            query: { detalhes: row.id },
                          })
                        }
                      >
                        <BuildCircleIcon color="primary" />
                      </IconButton>
                    </TableCell>
                    <TableCell align="center">
                      <IconButton
                        color="primary"
                        aria-label="edit"
                        component="label"
                        onClick={() =>
                          router.push({
                            pathname: "/turma/editar",
                            query: { ...row },
                          })
                        }
                      >
                        <ModeEditIcon />
                      </IconButton>
                    </TableCell>
                  </TableRow>
                ))}
              </TableBody>
            </Table>
            <Button
              variant="outlined"
              onClick={() => router.push("/turma/cadastro")}
              sx={{ alignSelf: "center" }}
            >
              Cadastrar
            </Button>
          </div>
        </main>
      </Layout>
    </div>
  );
}